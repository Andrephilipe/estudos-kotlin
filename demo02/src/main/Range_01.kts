// Função principal que é o ponto de entrada do programa
fun main() {
  // O loop 'for' itera sobre todos os caracteres do intervalo de 'a' a 'x'.
  // A cada iteração, 'chars' assume o valor do próximo caractere no intervalo.
  for (chars in 'a'..'x') {
    // Imprime o valor atual de 'chars'.
    println(chars)
  }
}
